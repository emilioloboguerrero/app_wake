function resolveLibraryName(pdfName) {
  if (!pdfName) return '';
  // Strip leading "OPCIONAL HOMBRES: " / "OPCIONAL MUJERES: " / "A1: " / "A2: " / "A3: "
  const cleaned = String(pdfName)
    .replace(/^OPCIONAL\s+(HOMBRES|MUJERES):\s*/i, '')
    .replace(/^A\d:\s*/i, '')
    .toUpperCase()
    .trim();
  return NAME_MAP[cleaned] || cleaned;
}

